package com.example.recyclerviewhw.recycler;


public class MessageData {

    String remark;
    String message;
    String time;
    Integer msgNumber;

    public MessageData(String remark, String message, String time, Integer msgNumber) {
        this.remark = remark;
        this.message = message;
        this.time = time;
        this.msgNumber = msgNumber;
    }
}